#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ret;
	size_t	i;

	ret = (char *)malloc(sizeof(*ret) * (ft_strlen(s1) + 1));
	if (!ret)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		ret[i] = s1[i];
		i++;
	}
	ret[i] = 0;
	return (ret);
}
